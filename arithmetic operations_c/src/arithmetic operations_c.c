#include <stdio.h>
#include <stdlib.h>

int main() {

	int x1 = 1;
	int x2 = -1;

	printf("%.1f\n", 12.1 + 9);
	printf("%d\n", 12 - 9);
	printf("%d\n", 12 * 9);
	printf("%f\n", 12.0 / 9);
	printf("%d\n", 12 % 9);

	printf("%d\n", -x1);
	printf("%d\n", -x2);

	return 0;
}
